-- Current date: time: NOW , CURRENT_TIMESTAMP
SELECT NOW() AS Current_DateTime;
SELECT CURRENT_TIMESTAMP() AS Current_DateTime;
SELECT SYSDATE() AS Current_DateTime;
SELECT CURDATE() AS CurrentDate;
SELECT CURTIME() AS CurrentTime;

-- AGE: DATEDIFF , TIMESTAMPDIFF
SELECT DATEDIFF('2026-07-10', '2026-07-08') AS Days;
SELECT TIMESTAMPDIFF(YEAR, '2000-01-01', CURDATE()) AS Age;

-- EXTRACT
SELECT EXTRACT(YEAR FROM '2026-07-08') AS Year;
SELECT EXTRACT(MONTH FROM '2026-07-08') AS Month;
SELECT EXTRACT(DAY FROM '2026-07-08') AS Day;
SELECT EXTRACT(HOUR FROM '2026-07-08 14:30:45') AS Hour;
SELECT EXTRACT(MINUTE FROM '2026-07-08 14:30:45') AS Minute;
SELECT EXTRACT(SECOND FROM '2026-07-08 14:30:45') AS Second; 
SELECT DAYNAME('2026-07-08') AS Day_Name;
SELECT MONTHNAME('2026-07-08') AS Month_Name;
